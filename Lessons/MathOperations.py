x = 4
y = 6


# Type casting int variables to float
x = float(x)
y = float(y)



# Addition
print(x + y)

# Subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / y)

# Floor division (Rounds down to nearest whole number OR lowest negative number)
print(x // y)

# Modulus (remainder)
print(x % y)

# Exponentiation (to the power of)
print(x ** y)