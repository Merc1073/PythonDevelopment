floatNum = 1.8
print(floatNum)

# convert to integer
floatNum = int(floatNum)
print(floatNum)

# convert to float
floatNum = float(floatNum)
print(floatNum)

# convert to string
floatNum = str(floatNum)
print(floatNum)

# convert to boolean (non-zero numbers/non-empty = True)
floatNum = bool(floatNum)
print(floatNum)